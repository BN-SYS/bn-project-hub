def build_sheet(wb, sheet_name, tab_color, hdr_bg, group_bg, tc_data):
    ws = wb.create_sheet(title=sheet_name)
    ws.sheet_properties.tabColor = tab_color
    ws.sheet_view.showGridLines = False

    apply_header(ws, hdr_bg)

    row = 2
    tc_no = 1

    for group_label, tc_list in tc_data:
        write_group(ws, row, group_label, group_bg)
        row += 1

        for tc in tc_list:
            (screen_id, screen_name, tc_name, pre, steps, expected, priority) = tc
            write_tc(ws, row, tc_no, screen_id, screen_name,
                     tc_name, pre, steps, expected, priority,
                     even=(tc_no % 2 == 0))
            tc_no += 1
            row += 1

    # 결과 열 드롭다운 (데이터 유효성)
    from openpyxl.worksheet.datavalidation import DataValidation
    dv = DataValidation(type="list",
                        formula1='"Pass,Fail,N/A"',
                        allow_blank=True,
                        showDropDown=False)
    ws.add_data_validation(dv)
    dv.add(f"I2:I{row}")

    return ws


