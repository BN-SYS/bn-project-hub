def mk_fill(hex_color):
    return PatternFill("solid", fgColor=hex_color)

def hdr_font(hex_color="FFFFFF", bold=True, size=10):
    return Font(name="맑은 고딕", bold=bold, color=hex_color, size=size)

def body_font(bold=False, size=9):
    return Font(name="맑은 고딕", bold=bold, size=size)

def wrap_align(h="left", v="top"):
    return Alignment(horizontal=h, vertical=v, wrap_text=True)

def center_align():
    return Alignment(horizontal="center", vertical="center", wrap_text=True)

def apply_header(ws, hex_bg):
    for ci, (col_name, col_w) in enumerate(COLS, 1):
        cell = ws.cell(row=1, column=ci, value=col_name)
        cell.fill = mk_fill(hex_bg)
        cell.font = hdr_font()
        cell.alignment = center_align()
        cell.border = BORDER
        ws.column_dimensions[get_column_letter(ci)].width = col_w
    ws.row_dimensions[1].height = 28
    ws.freeze_panes = "A2"

def write_group(ws, row, label, hex_bg, hex_font="333333"):
    cell = ws.cell(row=row, column=1, value=label)
    cell.fill = mk_fill(hex_bg)
    cell.font = Font(name="맑은 고딕", bold=True, size=9, color=hex_font)
    cell.alignment = center_align()
    ws.merge_cells(start_row=row, start_column=1,
                   end_row=row, end_column=len(COLS))
    for ci in range(1, len(COLS)+1):
        ws.cell(row=row, column=ci).border = BORDER
    ws.row_dimensions[row].height = 20

def write_tc(ws, row, no, screen_id, screen_name,
             tc_name, pre, steps, expected, priority,
             even=False):
    vals = [no, screen_id, screen_name, tc_name,
            pre, steps, expected, priority, "", "", ""]
    bg = CLR["row_even"] if even else CLR["white"]
    p_color = {"상": CLR["p_high"], "중": CLR["p_mid"], "하": CLR["p_low"]}.get(priority, CLR["white"])

    for ci, val in enumerate(vals, 1):
        cell = ws.cell(row=row, column=ci, value=val)
        cell.border = BORDER
        cell.font = body_font()
        cell.alignment = wrap_align()

        if ci == 1:   # No.
            cell.alignment = center_align()
            cell.fill = mk_fill(bg)
        elif ci == 2:  # 화면 ID
            cell.alignment = center_align()
            cell.fill = mk_fill(bg)
        elif ci == 8:  # 우선순위
            cell.fill = mk_fill(p_color)
            cell.alignment = center_align()
            cell.font = body_font(bold=True)
        elif ci == 9:  # 결과
            cell.fill = mk_fill(CLR["na_bg"])
            cell.alignment = center_align()
        else:
            cell.fill = mk_fill(bg)

    ws.row_dimensions[row].height = None  # auto

